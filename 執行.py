def calculate_performance_metrics(pnl_df):
    metrics = {}
    ann_factor = 1095

    for col in ['D1_Return', 'D2_Return', 'D3_Return', 'D4_Return', 'Long_Short_Return']:
        group_name = col.replace('_Return', '')
        rets = pnl_df[col]


        cum_wealth = (1 + rets).cumprod()
        total_wealth_multiple = cum_wealth.iloc[-1]

        ann_ret_geo = (total_wealth_multiple) ** (ann_factor / len(rets)) - 1

        ann_vol = rets.std() * np.sqrt(ann_factor)
        ir = ann_ret_geo / ann_vol if ann_vol != 0 else 0
        win_rate = (rets > 0).sum() / len(rets)

        #動態最大回撤
        running_max = cum_wealth.cummax()
        drawdown = (cum_wealth - running_max) / running_max
        max_dd = drawdown.min()

        metrics[group_name] = {
            '年化收益率': f"{ann_ret_geo:.2%}",
            '年化波動度': f"{ann_vol:.2%}",
            '資訊比率(IR)': f"{ir:.4f}",
            '單期勝率': f"{win_rate:.2%}",
            '最大回撤(MDD)': f"{max_dd:.2%}"
        }
    return pd.DataFrame(metrics).T

