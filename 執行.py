def backtest_model_predictions(pred_matrix, real_return_matrix, timestamps):
    #來記錄各組每期真實回報
    d1_returns, d2_returns, d3_returns, d4_returns = [], [], [], []

    for t in timestamps:
        #抓出當期20檔幣的預測得分與真實報酬率
        t_preds = pred_matrix.loc[t].dropna()
        t_real_rets = real_return_matrix.loc[t]

        if t_preds.empty:
            continue

        #橫截面排序
        sorted_symbols = t_preds.sort_values(ascending=False).index.tolist()
        n_symbols = len(sorted_symbols)

        #四等分
        q = n_symbols // 4
        group_d1 = sorted_symbols[:q]
        group_d2 = sorted_symbols[q:2*q]
        group_d3 = sorted_symbols[2*q:3*q]
        group_d4 = sorted_symbols[3*q:]

        #真實回報下一期的真正損益，計算等權重平均數
        d1_returns.append(t_real_rets.loc[group_d1].mean())
        d2_returns.append(t_real_rets.loc[group_d2].mean())
        d3_returns.append(t_real_rets.loc[group_d3].mean())
        d4_returns.append(t_real_rets.loc[group_d4].mean())

    #整合時序DataFrame
    pnl_df = pd.DataFrame({
        'D1_Return': d1_returns,
        'D2_Return': d2_returns,
        'D3_Return': d3_returns,
        'D4_Return': d4_returns
    }, index=timestamps)

    #多空對沖組合損益
    pnl_df['Long_Short_Return'] = pnl_df['D1_Return'] - pnl_df['D4_Return']

    #計算累積收益率
    pnl_df['Cum_D1'] = (1 + pnl_df['D1_Return']).cumprod() - 1
    pnl_df['Cum_D2'] = (1 + pnl_df['D2_Return']).cumprod() - 1
    pnl_df['Cum_D3'] = (1 + pnl_df['D3_Return']).cumprod() - 1
    pnl_df['Cum_D4'] = (1 + pnl_df['D4_Return']).cumprod() - 1
    pnl_df['Cum_Long_Short'] = (1 + pnl_df['Long_Short_Return']).cumprod() - 1

    return pnl_df


#3雙線路並行回測
