import pandas as pd
import numpy as np
import scipy.stats as stats
import warnings

warnings.filterwarnings('ignore')


#1.載入整理歷史Ridge係數路徑

if 'linear_coef_history' not in globals() or len(linear_coef_history) == 0:
    raise NameError("錯誤")

feature_cols = ['絕對基差', '基差速度', '微觀波動度', '微觀相關性', '7D基差ZScore', '合約非流動性']
feature_eng_names = ['feat_basis_abs', 'feat_basis_speed', 'feat_basis_vol', 'feat_price_corr', 'feat_basis_zscore', 'feat_illiquidity']

# 轉換成DataFrame
coef_series_df = pd.DataFrame(linear_coef_history)
coef_series_df.columns = ['timestamp'] + feature_eng_names
coef_series_df = coef_series_df.set_index('timestamp').sort_index()


# 2.係數穩健性統計
ml_summary = pd.DataFrame(index=feature_cols)
ml_summary['歷史平均係數 (Mean_beta)'] = coef_series_df.mean().values
ml_summary['係數標準差 (Std_beta)'] = coef_series_df.std().values

#計算符號一致性 係數與平均值同號的時間佔比
ml_summary['符號一致性比率'] = [
    np.mean(np.sign(coef_series_df[col]) == np.sign(coef_series_df[col].mean()))
    for col in feature_eng_names
]
ml_summary['符號一致性比率'] = ml_summary['符號一致性比率'].map(lambda x: f"{x:.2%}")

# 3.Fama-MacBeth 顯著性檢定

t_stats = []
p_values = []

for col in feature_eng_names:
    series = coef_series_df[col].dropna()
    t_val, p_val = stats.ttest_1samp(series, 0)
    t_stats.append(t_val)
    p_values.append(p_val)

ml_summary['Fama-MacBeth_t-stat'] = t_stats
ml_summary['p-value'] = p_values
ml_summary['統計顯著性 (95% 水準)'] = ml_summary['p-value'].map(lambda x: '顯著' if x < 0.05 else '不顯著')

#格式化輸出
ml_summary['歷史平均係數 (Mean_beta)'] = ml_summary['歷史平均係數 (Mean_beta)'].map(lambda x: f"{x:.6f}")
ml_summary['係數標準差 (Std_beta)'] = ml_summary['係數標準差 (Std_beta)'].map(lambda x: f"{x:.6f}")
ml_summary['p-value'] = ml_summary['p-value'].map(lambda x: f"{x:.4e}" if x < 0.0001 else f"{x:.4f}")
ml_summary['Fama-MacBeth_t-stat'] = ml_summary['Fama-MacBeth_t-stat'].map(lambda x: f"{x:.4f}")


#4.成果
print("======================================================================")
print("Ridge 因子歷史係數與 Fama-MacBeth 顯著性檢定綜合表：")
print("==========================================================================")
