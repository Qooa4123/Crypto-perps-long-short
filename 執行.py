#4.ExpandingWindow迴圈

for idx in tqdm(range(start_idx, end_idx), desc="預報中"):
    current_t = timestamps[idx]

    # 判定當期Universe標的名單
    current_week_str = current_t.strftime('%Y-W%W')
    if current_week_str not in universe_history:
        avail_weeks = [w for w in universe_history.keys() if w <= current_week_str]
        current_week_str = avail_weeks[-1] if avail_weeks else list(universe_history.keys())[0]
    top_20_symbols = universe_history[current_week_str]

    #擷取截至t-1，並過濾當期Universe
    history_ts = timestamps[:idx]
    hist_data_scaled = ml_panel_scaled.loc[history_ts]
    hist_data_scaled = hist_data_scaled[hist_data_scaled.index.get_level_values('symbol').isin(top_20_symbols)].dropna()

    if hist_data_scaled.empty or len(hist_data_scaled.index.get_level_values('timestamp').unique()) < 10:
        continue

    #動態等比例切分
    unique_hist_times = hist_data_scaled.index.get_level_values('timestamp').unique().sort_values()
    split_point = int(len(unique_hist_times) * (2/3))

    train_df = hist_data_scaled.loc[unique_hist_times[:split_point]]
    val_df = hist_data_scaled.loc[unique_hist_times[split_point:]]

    X_train, y_train = train_df[feature_cols], train_df['target_y']
    X_val, y_val = val_df[feature_cols], val_df['target_y']

    #Ridge調優Lambda
    best_alpha, best_lin_mse = 1.0, float('inf')
    for alpha_cand in ridge_alphas:
        model_lin = Ridge(alpha=alpha_cand, random_state=42)
        model_lin.fit(X_train, y_train)
        mse = np.mean((y_val - model_lin.predict(X_val)) ** 2)
        if mse < best_lin_mse:
            best_lin_mse = mse
            best_alpha = alpha_cand

    #歷史合體擬合
    final_lin = Ridge(alpha=best_alpha, random_state=42)
    final_lin.fit(hist_data_scaled[feature_cols], hist_data_scaled['target_y'])


    #LightGBM深度與葉子網格搜尋
    best_leaves, best_depth, best_lgb_mse = 15, 4, float('inf')
    for depth_cand in lgb_depths:
        for leaves_cand in lgb_leaves:
            if leaves_cand >= (2 ** depth_cand):
                continue

            model_lgb = lgb.LGBMRegressor(num_leaves=leaves_cand, max_depth=depth_cand, learning_rate=0.05,
                                          n_estimators=30, random_state=42, verbose=-1)
            model_lgb.fit(X_train, y_train)
            mse = np.mean((y_val - model_lgb.predict(X_val)) ** 2)
            if mse < best_lgb_mse:
                best_lgb_mse = mse
                best_leaves = leaves_cand
                best_depth = depth_cand

    #全歷史合體擬合
    final_lgb = lgb.LGBMRegressor(num_leaves=best_leaves, max_depth=best_depth, learning_rate=0.05,
                                  n_estimators=30, random_state=42, verbose=-1)
    final_lgb.fit(hist_data_scaled[feature_cols], hist_data_scaled['target_y'])


    #歷史軌跡快照
    hyperparam_history.append({
        'timestamp': current_t,
        'best_lambda': best_alpha,
        'best_leaves': best_leaves,
        'best_depth': best_depth
    })
    linear_coef_history.append(np.append([current_t], final_lin.coef_))
    nonlinear_importance_history.append(np.append([current_t], final_lgb.feature_importances_))


    # 4g.當前第t期外推預測
    current_sample_scaled = ml_panel_scaled.loc[[current_t]]
    current_sample_scaled = current_sample_scaled[current_sample_scaled.index.get_level_values('symbol').isin(top_20_symbols)]

    if not current_sample_scaled.empty:
        X_test = current_sample_scaled[feature_cols]
        target_symbols = current_sample_scaled.index.get_level_values('symbol')

        #輸出模型對未來8H合約實現報酬率的預測值，填表
        pred_linear_matrix.loc[current_t, target_symbols] = final_lin.predict(X_test)
        pred_nonlinear_matrix.loc[current_t, target_symbols] = final_lgb.predict(X_test)


print(f"線性預測矩陣有效區間維度: {pred_linear_matrix.loc[timestamps[start_idx]:timestamps[end_idx-1]].dropna(how='all').shape}")
print(f"非線性預測矩陣有效區間維度: {pred_nonlinear_matrix.loc[timestamps[start_idx]:timestamps[end_idx-1]].dropna(how='all').shape}")

