#2.5m微觀聚合->8H降頻
def process_symbol_features(symbol):
    #讀取並合併期貨3年5m數據
    f_month_dfs = []
    for f_file in futures_map[symbol]:
        try:
            with zipfile.ZipFile(os.path.join(futures_dir, f_file), 'r') as z:
                csv = f_file.replace('.zip', '.csv')
                df = pd.read_csv(z.open(csv), header=None, usecols=[0, 4, 5])
                df.columns = ['timestamp', 'f_close', 'f_volume']
                df = df[df['timestamp'] != 'open_time']
                f_month_dfs.append(df)
        except: pass

    #讀取並合併現貨3年5m數據
    s_month_dfs = []
    for s_file in sport_map[symbol]:
        try:
            with zipfile.ZipFile(os.path.join(spot_dir, s_file), 'r') as z:
                csv = s_file.replace('.zip', '.csv')
                df = pd.read_csv(z.open(csv), header=None, usecols=[0, 4])
                df.columns = ['timestamp', 's_close']
                df = df[df['timestamp'] != 'open_time']
                s_month_dfs.append(df)
        except: pass

    if not f_month_dfs or not s_month_dfs: return None

    f_df = pd.concat(f_month_dfs, ignore_index=True)
    s_df = pd.concat(s_month_dfs, ignore_index=True)

    for df_raw in [f_df, s_df]: # Renamed loop variable to avoid confusion
        for col in df_raw.columns:
            df_raw[col] = pd.to_numeric(df_raw[col], errors='coerce')

        df_raw['timestamp'] = pd.to_datetime(df_raw['timestamp'], unit='ms', errors='coerce')
        df_raw.dropna(subset=['timestamp'], inplace=True)

        df_raw = df_raw[(df_raw['timestamp'] >= '2023-01-01') & (df_raw['timestamp'] <= '2026-01-01')]

    f_df = f_df.set_index('timestamp').sort_index()
    s_df = s_df.set_index('timestamp').sort_index()


    merged_5m = pd.merge_asof(
        f_df.reset_index(),
        s_df.reset_index(),
        on='timestamp',
        direction='nearest',
        tolerance=pd.Timedelta('15s')
    ).set_index('timestamp')
    if merged_5m.empty: return None

    merged_5m['f_return'] = merged_5m['f_close'].pct_change().abs()
    merged_5m['basis_5m'] = (merged_5m['f_close'] - merged_5m['s_close']) / merged_5m['s_close']
    merged_5m['amihud_raw'] = np.where(merged_5m['f_volume'] > 0, merged_5m['f_return'] / merged_5m['f_volume'], 0)

    #降頻聚合
    resampled_8h = pd.DataFrame(index=merged_5m['f_close'].resample('8h').last().index)
    resampled_8h['f1_basis'] = merged_5m['basis_5m'].resample('8h').last()
    resampled_8h['f2_delta_basis'] = resampled_8h['f1_basis'].diff()
    resampled_8h['f3_basis_std'] = merged_5m['basis_5m'].resample('8h').std()

    def calc_corr(window):
        if len(window) < 10: return np.nan
        return window['f_close'].corr(window['s_close'])
    resampled_8h['f4_price_corr'] = merged_5m[['f_close', 's_close']].resample('8h').apply(calc_corr)
    resampled_8h['f6_illiquidity'] = merged_5m['amihud_raw'].resample('8h').mean()

    rolling_mean = resampled_8h['f1_basis'].rolling(window=21, min_periods=5).mean()
    rolling_std = resampled_8h['f1_basis'].rolling(window=21, min_periods=5).std()
    resampled_8h['f5_basis_zscore'] = np.where(rolling_std > 0, (resampled_8h['f1_basis'] - rolling_mean) / rolling_std, 0)

    resampled_8h = resampled_8h.ffill().fillna(0)
    resampled_8h['symbol'] = symbol
    return resampled_8h.reset_index()


