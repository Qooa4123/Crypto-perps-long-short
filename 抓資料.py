#掃描期現所有ZIP
f_files = [f for f in os.listdir(futures_dir) if f.endswith('.zip')]
s_files = [f for f in os.listdir(spot_dir) if f.endswith('.zip')]

#幣種->月份檔案
futures_map = defaultdict(list)
for f in f_files:
    symbol = f.split('-')[0]
    futures_map[symbol].append(f)

sport_map = defaultdict(list)
for f in s_files:
    symbol = f.split('-')[0]
    sport_map[symbol].append(f)

#找出期貨與現貨同時存在的交集幣
valid_symbols = list(set(futures_map.keys()) & set(sport_map.keys()))
